DROP TABLE IF EXISTS LX_ADMIN;
CREATE TABLE IF NOT EXISTS LX_ADMIN (
	ID bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键' , 
	GMT_CREATED datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
	GMT_LASTLOGIN datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
	USERNAME varchar(50) NOT NULL,
	PASSWORD varchar(50) NOT NULL,
	NICKNAME varchar(50) NOT NULL,
	TEL varchar(50),
	EMAIL varchar(50),
	STATUS varchar(50),
	MANAGER varchar(50),
	GMT_MANAGED datetime,
	PRIMARY KEY (ID),
	UNIQUE UNIQUE_USERNAME (USERNAME)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;




DROP TABLE IF EXISTS LX_ADMIN_EXT;
CREATE TABLE IF NOT EXISTS LX_ADMIN_EXT (
	ID bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键' , 
	USER_ID bigint(20) NOT NULL,
	EXT_KEY varchar(50) NOT NULL,
